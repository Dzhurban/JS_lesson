                                                    // Операция 1: Конкатенация строк и чисел.
                                                   // 'number' + 3 преобразует 'number' в строку и добавляет к ней 3.
                                                  // Затем к полученной строке добавляется числовое значение 3, результат - 'number33'.
                                                  console.log('number' + 3 + 3); // Результат: 'number33'

                                                  // Операция 2: Сложение с null.
                                                 // null преобразуется в число 0, поэтому результатом сложения будет 3.
console.log(null + 3); // Результат: 3

                                                 // Операция 3: Логическое И и возврат значения, которое не является false.
                                                // 5 и "qwerty" оба истинные, поэтому результатом будет "qwerty".
console.log(5 && "qwerty"); // Результат: "qwerty"

                                                // Операция 4: Сложение чисел и строки.
                                               // Строки '40' и '2' конвертируются в числа 40 и 2 соответственно.
                                               // Затем эти числа складываются вместе, получаем 42.
                                               // К результату прибавляется строка 'hillel', получаем '42hillel'.
console.log(+'40' + +'2' + "hillel"); // Результат: '42hillel'

                                                 // Операция 5: Сравнение с вычитанием.
                                                // '10' преобразуется в число 10, затем происходит сравнение с числом 6, результат - false.
console.log('10' - 5 === 6); // Результат: false

                                                 // Операция 6: Сложение true и false.
                                                // true преобразуется в 1, а false - в 0, поэтому результат - 1.
console.log(true + false); // Результат: 1

                                                // Операция 7: Вычитание из строки.
                                                // Строка '4px' не может быть преобразована в число, поэтому получаем NaN (Not-a-Number).
console.log('4px' - 3); // Результат: NaN

                                                 // Операция 8: Вычитание строк.
                                                // '4' преобразуется в число 4, вычитаем из него 3, получаем 1.
console.log('4' - 3); // Результат: 1

                                                  // Операция 9: Сложение строки и степени числа.
                                                 // Выражение 3 ** 0 даёт 1.
                                                // Затем строка '6' конкатенируется с числом 1, получаем '61'.
console.log('6' + 3 ** 0); // Результат: 61

                                                 // Операция 10: Деление строки на число.
                                                 // Строка '6' преобразуется в число 6, после чего происходит деление числа 12 на 6, результат - 2.
console.log(12 / '6'); // Результат: 2

                                                  // Операция 11: Сложение строки и результата сравнения.
                                                 // 5 === 6 дает false, который конкатенируется со строкой '10', результат - '10false'.
console.log('10' + (5 === 6)); // Результат: '10false'

                                                  // Операция 12: Сравнение null и пустой строки.
                                                 // null не равен пустой строке, результат - false.
console.log(null == ''); // Результат: false

                                                   // Операция 13: Возведение в степень.
                                                  // 9 / 3 даёт 3, возводим 3 в степень 3, получаем 27.
console.log(3 ** (9 / 3)); // Результат: 27

                                                 // Операция 14: Логическое сравнение двух строк.
                                                // Строки 'false' и 'true' преобразуются в true, результат - true.
console.log(!!'false' == !!'true'); // Результат: true

                                                // Операция 15: Логическое ИЛИ и И.
                                               // 0 преобразуется в false, '0' преобразуется в true, результат - '0'.
console.log(0 || '0' && 1); // Результат: '0'

                                              // Операция 16: Сравнение числа с логическим значением.
                                             // null преобразуется в 0, false преобразуется в 0, результат сравнения false с 1 - false.
console.log((+null == false) < 1); // Результат: false

                                              // Операция 17: Логическое ИЛИ или И.
                                             // false && true даёт false, false || true даёт true.
console.log(false && true || true); // Результат: true

                                             // Операция 18: Логическое И или ИЛИ.
                                            // false || true даёт true.
console.log(false && (false || true)); // Результат: false

                                            // Операция 19: Сравнение числа с возведением в степень.
                                           // (+null == false) даёт true, а true возведённое в степень 5, даёт 1.
console.log((+null == false) < 1 ** 5); // Результат: true
